import type { JobPackAssetRecord, JobPackDocumentModel, JobPackRisk, JobPackSection } from './jobPackTypes';
import { buildJobPackMapPdfHtml } from './jobPackMapPdfEngine';

export type JobPackDownloadFormat = 'zip' | 'html' | 'json';

export interface JobPackDownloadFile {
  path: string;
  mimeType: string;
  content: string;
}

export interface JobPackDownloadBundle {
  fileName: string;
  files: JobPackDownloadFile[];
}

const textEncoder = new TextEncoder();

function safeFileName(value: string): string {
  return (value || 'job-pack')
    .replace(/[\\/:*?"<>|]+/g, '-')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .toUpperCase();
}

function csvCell(value: unknown): string {
  const text = value === null || value === undefined ? '' : String(value);
  return `"${text.replace(/"/g, '""')}"`;
}

function csvRow(values: unknown[]): string {
  return values.map(csvCell).join(',');
}

function escapeHtml(value: unknown): string {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function sectionToMarkdown(section: JobPackSection): string {
  return [`## ${section.title}`, '', ...section.lines.map((line) => `- ${line}`), ''].join('\n');
}

function assetRegisterCsv(assets: JobPackAssetRecord[]): string {
  const rows = [
    csvRow(['Asset ID', 'Name', 'Type', 'Status', 'Install Method', 'Fibre Count', 'Cable Type', 'Geometry', 'Work Instruction', 'Validation Notes']),
    ...assets.map((asset) => csvRow([
      asset.id,
      asset.name,
      asset.type,
      asset.status || '',
      asset.installMethod || '',
      asset.fibreCount || '',
      asset.cableType || '',
      asset.geometrySummary,
      asset.workInstruction,
      asset.validationNotes.join(' | '),
    ])),
  ];
  return rows.join('\n');
}

function riskRegisterCsv(risks: JobPackRisk[]): string {
  const rows = [
    csvRow(['Risk ID', 'Level', 'Title', 'Asset ID', 'Asset Name', 'Message', 'Recommended Action']),
    ...risks.map((risk) => csvRow([
      risk.id,
      risk.level,
      risk.title,
      risk.assetId || '',
      risk.assetName || '',
      risk.message,
      risk.recommendedAction,
    ])),
  ];
  return rows.join('\n');
}

function jobPackMarkdown(jobPack: JobPackDocumentModel): string {
  return [
    `# ${jobPack.jobPackNumber}`,
    '',
    `Area: ${jobPack.areaName || jobPack.areaId}`,
    `Revision: ${jobPack.revisionNumber || 'Live Draft'}`,
    `Status: ${jobPack.status.replace(/_/g, ' ')}`,
    `Generated: ${jobPack.generatedAt}`,
    `Generated By: ${jobPack.generatedBy || 'Unknown'}`,
    `Issued: ${jobPack.issuedAt || 'Not issued'}`,
    `Reason: ${jobPack.reason}`,
    '',
    '## Summary',
    '',
    `- Total assets: ${jobPack.summary.totalAssets}`,
    `- Poles: ${jobPack.summary.poles}`,
    `- Chambers: ${jobPack.summary.chambers}`,
    `- DPs: ${jobPack.summary.distributionPoints}`,
    `- Joints: ${jobPack.summary.joints}`,
    `- Cables: ${jobPack.summary.cables}`,
    `- Homes: ${jobPack.summary.homes}`,
    `- Warnings: ${jobPack.summary.warnings}`,
    `- Blockers: ${jobPack.summary.blockers}`,
    '',
    ...jobPack.sections.map(sectionToMarkdown),
  ].join('\n');
}

