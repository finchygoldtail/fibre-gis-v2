export function buildJobPackDownloadBundle(jobPack: JobPackDocumentModel): JobPackDownloadBundle {
  const base = safeFileName(jobPack.jobPackNumber);
  return {
    fileName: `${base}.zip`,
    files: [
      { path: `${base}/README.md`, mimeType: 'text/markdown', content: jobPackMarkdown(jobPack) },
      { path: `${base}/Job-Pack-Map-Print.html`, mimeType: 'text/html', content: buildJobPackMapPdfHtml(jobPack) },
      { path: `${base}/Job-Pack-Register.html`, mimeType: 'text/html', content: buildJobPackHtml(jobPack) },
      { path: `${base}/registers/Asset-Register.csv`, mimeType: 'text/csv', content: assetRegisterCsv(jobPack.assets) },
      { path: `${base}/registers/Issue-Checks.csv`, mimeType: 'text/csv', content: riskRegisterCsv(jobPack.risks) },
      { path: `${base}/data/Job-Pack.json`, mimeType: 'application/json', content: JSON.stringify(jobPack, null, 2) },
    ],
  };
}

function makeCrcTable(): Uint32Array {
  const table = new Uint32Array(256);
  for (let n = 0; n < 256; n += 1) {
    let c = n;
    for (let k = 0; k < 8; k += 1) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    table[n] = c >>> 0;
  }
  return table;
}

const crcTable = makeCrcTable();

function crc32(bytes: Uint8Array): number {
  let crc = 0xffffffff;
  for (let i = 0; i < bytes.length; i += 1) crc = crcTable[(crc ^ bytes[i]) & 0xff] ^ (crc >>> 8);
  return (crc ^ 0xffffffff) >>> 0;
}

function dosDateTime(date = new Date()): { time: number; date: number } {
  const time = (date.getHours() << 11) | (date.getMinutes() << 5) | Math.floor(date.getSeconds() / 2);
  const dosDate = ((date.getFullYear() - 1980) << 9) | ((date.getMonth() + 1) << 5) | date.getDate();
  return { time, date: dosDate };
}

function u16(value: number): Uint8Array {
  return new Uint8Array([value & 0xff, (value >>> 8) & 0xff]);
}

function u32(value: number): Uint8Array {
  return new Uint8Array([value & 0xff, (value >>> 8) & 0xff, (value >>> 16) & 0xff, (value >>> 24) & 0xff]);
}

function concatBytes(chunks: Uint8Array[]): Uint8Array {
  const length = chunks.reduce((total, chunk) => total + chunk.length, 0);
  const output = new Uint8Array(length);
  let offset = 0;
  chunks.forEach((chunk) => {
    output.set(chunk, offset);
    offset += chunk.length;
  });
  return output;
}

export function createZipBlob(files: JobPackDownloadFile[]): Blob {
  const localChunks: Uint8Array[] = [];
  const centralChunks: Uint8Array[] = [];
  let offset = 0;
  const stamp = dosDateTime();

  files.forEach((file) => {
    const fileNameBytes = textEncoder.encode(file.path);
    const contentBytes = textEncoder.encode(file.content);
    const checksum = crc32(contentBytes);
    const localHeader = concatBytes([
      u32(0x04034b50),
      u16(20),
      u16(0x0800),
      u16(0),
      u16(stamp.time),
      u16(stamp.date),
      u32(checksum),
      u32(contentBytes.length),
      u32(contentBytes.length),
      u16(fileNameBytes.length),
      u16(0),
      fileNameBytes,
    ]);

    localChunks.push(localHeader, contentBytes);

    const centralHeader = concatBytes([
      u32(0x02014b50),
      u16(20),
      u16(20),
      u16(0x0800),
      u16(0),
      u16(stamp.time),
      u16(stamp.date),
      u32(checksum),
      u32(contentBytes.length),
      u32(contentBytes.length),
      u16(fileNameBytes.length),
      u16(0),
      u16(0),
      u16(0),
      u16(0),
      u32(0),
      u32(offset),
      fileNameBytes,
    ]);
    centralChunks.push(centralHeader);
    offset += localHeader.length + contentBytes.length;
  });

  const centralDirectory = concatBytes(centralChunks);
  const endRecord = concatBytes([
    u32(0x06054b50),
    u16(0),
    u16(0),
    u16(files.length),
    u16(files.length),
    u32(centralDirectory.length),
    u32(offset),
    u16(0),
  ]);

  return new Blob([concatBytes([...localChunks, centralDirectory, endRecord])], { type: 'application/zip' });
}

function triggerDownload(blob: Blob, fileName: string): void {
  if (typeof document === 'undefined') return;
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  window.setTimeout(() => URL.revokeObjectURL(url), 500);
}

export function downloadJobPackZip(jobPack: JobPackDocumentModel): void {
  const bundle = buildJobPackDownloadBundle(jobPack);
  triggerDownload(createZipBlob(bundle.files), bundle.fileName);
}

export function downloadJobPackHtml(jobPack: JobPackDocumentModel): void {
  const base = safeFileName(jobPack.jobPackNumber);
  triggerDownload(new Blob([buildJobPackMapPdfHtml(jobPack)], { type: 'text/html' }), `${base}-MAP-PDF.html`);
}

export function downloadJobPackJson(jobPack: JobPackDocumentModel): void {
  const base = safeFileName(jobPack.jobPackNumber);
  triggerDownload(new Blob([JSON.stringify(jobPack, null, 2)], { type: 'application/json' }), `${base}.json`);
}
