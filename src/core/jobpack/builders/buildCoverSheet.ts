import type { BuildPartnerJobPackInput, BuildPartnerJobPackSection, BuildPartnerJobPackSummary } from '../jobPackModels';

export function buildCoverSheet(input: BuildPartnerJobPackInput, summary: BuildPartnerJobPackSummary): BuildPartnerJobPackSection {
  return {
    key: 'cover',
    title: 'Cover Sheet',
    fileName: '00_Cover_Sheet.md',
    lines: [
      `Area: ${input.areaName || input.areaId}`,
      `Revision: ${input.revisionNumber || 'LIVE'}`,
      `Generated by: ${input.generatedBy || 'Alistra GIS'}`,
      `Generated at: ${new Date().toLocaleString('en-GB')}`,
      `Reason: ${input.reason || 'Generated from live map.'}`,
      `Total assets: ${summary.totalAssets}`,
      `Warnings: ${summary.warnings}`,
      `Blockers: ${summary.blockers}`,
      'Controlled document: Build Partners must only use the latest issued pack.',
    ],
  };
}
